public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        
        //Para representar uma classe de texto:
        String meuNome = "Eduardo Henrique";
        
        //Representar números duplos:
        double salarioMinimo = 2.500;

        //Valores pequenos "short"
        short numeroCurto = 1;
        
        //Valores inteiros "int"
        int numeroNormal = numeroCurto;
        short numeroCurto2 = (short) numeroNormal;

        //Para que a variável nunca mude: escrever em caixa alta e acrescentar "final" antes de apresentar uma classe.
        
        final double VALOR_PI = 3.14;

    }
}
